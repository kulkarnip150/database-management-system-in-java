    public updateRowData(String tableName,String columnNames[],String[] values){
       /* tableName = tableName;
        columnNames = columnNames;
        values = values;*/
        size = columnNames.length;
        str = new String[size];
        jTextField = new JTextField[size];
        jLabel1 = new JLabel[size];
        submit = new JButton("Update");
        cancel = new JButton("Cancel");
        
        for(int i=0,y=50;i<size;i++){
            jLabel1[i] = new JLabel();
            jLabel1[i].setText(columnNames[i]);
            jLabel1[i].setBounds(30, y, 80, 40);
            add(jLabel1[i]);
            y+=50;
        }
        
        for(int i=0,y=50;i<size;i++){
            jTextField[i] = new JTextField(30);
            jTextField[i].setText(values[i]);
            jTextField[i].setBounds(110, y, 150, 40);
            add(jTextField[i]);
            y+=50;
        }
        
        setLayout(null);
        setTitle("View Rows");
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        
        submit.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                for(int i=0;i<size;i++){
                    str[i] = jTextField[i].getText();
                }
                DataBaseFunctions.updateRow(tableName,columnNames,values,str,size);
                for(int i=0;i<size;i++){
                    jTextField[i].setText("");
                }
            }
        });
        
        cancel.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                for(int i=0;i<size;i++){
                    jTextField[i].setText("");
                }
                dispose();
            }
        });
        
        submit.setBounds(130,480,150,40);
        cancel.setBounds(290,480,150,40);
        
        //add(sp);
        add(submit);
        add(cancel);
       
        setPreferredSize(new Dimension(600,600));
        pack();
        //setSize(1400,800);
        setLocationRelativeTo(null);
    }

